import { z } from "zod";
import { procedure, router } from "../trpc";

const trackRouter = router({
  hello: procedure
    .input(
      z.object({
        text: z.string(),
      })
    )
    .query(({ input }) => {
      return {
        greeting: `hello ${input.text}`,
      };
    }),
});

// export type definition of API
export type trackRouter = typeof trackRouter;
